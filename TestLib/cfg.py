cfgxmlTpl = Template("""<?xml version="1.0" encoding="UTF-8"?>
<hl_provision version="1">
    <config version="1">
$PValues
    </config>
</hl_provision>
""")

PValueTpl = Template(
    '       <$PNo para="$PStr" >$value</$PNo>'
)


class CfgGenerator(object):
    def __init__(self):
        pass

    @staticmethod
    def getPNo(PStr: str):
        """
        getPNo() -> str

        通过配置项描述获取相应P值

        :param PStr:P值描述

        :return:PNo：相应P值

        Examples:
        | ${PNo} | getPNo()  | Profile1_Sipserver |
        | log | ${PNo} |

        PNo = 'P47'
        """
        return CFG_MAPPING.get(PStr)

    @staticmethod
    def transforPStrToPNo(cfgs: dict):
        """
        将配置描述和配置值组成的字典转换成P值与配置值组成的字典

        transforPStrToPNo(cfgs: dict) -> dict

        :param cfgs:配置描述和配置值组成的字典
        :return:P值与配置值组成的字典

         Examples:
         | ${PNo-PValue} | transforPStrToPNo()  | ${PStr-PValue} |
        """
        values = dict()
        for k, v in cfgs.items():
            p = CFG_MAPPING.get(k)
            if not p:
                print("can not find cfg key %s" % k)
            else:
                values[p] = v
        return values

    def genBin(self, cfgs: dict):
        """
        根据配置描述和配置值组成的字典生成Bin配置文件

        genBin(self, cfgs: dict) -> str

        :param cfgs: 配置描述和配置值组成的字典

        :return: Bin文件字符串

        Examples:
         | ${BinStr} | genBin()  | ${PStr-PValue} |
        """
        values = list()
        for k, v in cfgs.items():
            p = CFG_MAPPING.get(k)
            if not p:
                print("can not find cfg key %s" % k)
            else:
                values.append("%s=%s" % (p, v))

        return 'Flag=5678&CheckSum=0&' + str.join('&', values) + '&end'

    def genXML(self, cfgs: dict):
        """
        根据配置描述和配置值组成的字典生成XML配置文件

        genXML(self, cfgs: dict) -> XML

        :param cfgs: 配置描述和配置值组成的字典

        :return: XML配置文件

        Examples:
         | ${XML} | genXML()  | ${PStr-PValue} |
        """
        values = list()
        xml = None
        for k, v in cfgs.items():
            p = CFG_MAPPING.get(k)
            if not p:
                print("can not find cfg key %s" % k)
            else:
                values.append(PValueTpl.substitute(PNo=p, PStr=k, value=v))

        xml = cfgxmlTpl.substitute(PValues=str.join('\n', values))

        print(xml)
        return xml
